언어를 배우는 가장 좋은 방법은 대화를 하는 것입니다.<br>

듣기, 읽기, 쓰기 등은 혼자서 충분히 할 수 있으며 동영상 강의나 학교, 학원에서도 할 수 있지만 대화는 반드시 상대가 있어야 합니다.<br>

하지만 같은 문화와 언어를 쓰고 같은 방식으로 외국어를 배운 사람들끼리 외국어로 실습 대화를 한다면 대화가 잘 이루어지 않고 제한된 화술을 사용 할 수 밖에 없습니다.<br>

언어 교환이란 자신의 모국어를 다름 사람에게 가르쳐주는 것입니다.<br>

이것은 자신이 도움받은 만큼 타인에게 도움을 주는 것 뿐만아니라

직접 언어를 가르쳐 보는 교육의 경험을 습득할 수 있으며, 가르치는 과정에서 배우는 상대방에게 틀린 부분을 지적하며 틀리는 것이 부끄럽지 않다는 것을 스스로 배우게 되는 것입니다.<br>

그래서 자신이 외국어를 말할 때 틀려도 당황하지 않고 침착하게 대화를 계속 진행하게 되는 것입니다.

